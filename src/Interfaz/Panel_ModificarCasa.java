    public void modificar()
    {
        int cuartos, banos, mes, deposito;
        String codigo,provincia, canton, barrio, dirrecion;
        boolean cochera, patio, amueblado, agua, luz, internet, cable;
        cuartos = Integer.parseInt(jTextFieldCuartos.getText());
        banos = Integer.parseInt(jTextFieldBanos2.getText());
        mes = Integer.parseInt(jTextFieldMes2.getText());
        deposito = Integer.parseInt(jTextFieldDepo2.getText());
        // me dejo los strings
        codigo = jLabelCodigo.getText();
        provincia = jTextFieldProvi2.getText();
        canton = jTextFieldCanton2.getText();
        barrio = jTextFieldBarrio2.getText();
        dirrecion = jTextFieldDireccion2.getText();
        // me dejo los bool
        cochera = jCheckBoxCochera2.isSelected();
        patio = jCheckBoxPatio2.isSelected();
        amueblado = jCheckBoxAmueblado2.isSelected();
        agua = jCheckBoxAgua2.isSelected();
        luz = jCheckBoxLuz2.isSelected();
        internet = jCheckBoxInternet2.isSelected();
        cable = jCheckBoxCable2.isSelected();       
        Casa casa = new Casa(codigo,cuartos,banos,mes,deposito,provincia,canton,barrio,dirrecion,cochera,patio,amueblado,agua,luz,internet,cable); 
        JOptionPane.showMessageDialog(null, logica.modificar(casa,this.codigo), "Modificaci√≥n", 1);
        
    }
